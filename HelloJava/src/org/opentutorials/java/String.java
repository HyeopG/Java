package org.opentutorials.java;

public class String {

	public static void main(java.lang.String[] args) {
		System.out.println("안녕하세요.");	// 문자열 출력가능.
		
		System.out.println('고' + "기먹고싶다.");	// 문자 + 문자열도 출력가능.
		
		System.out.println("고기" + "먹을래");		// 문자열 + 문자열도 가능!
		
		System.out.println("egoing said \"Welcome programming world\""); // 따음표를 출력하는 방법!
	}

}
